// PDF generator for Form 8949, Schedule D, Schedule 1
// Uses pdf-lib for pure JS PDF generation

import { PDFDocument, rgb, StandardFonts } from 'pdf-lib';
import fs from 'fs';
import path from 'path';

const GRAY = rgb(0.4, 0.4, 0.4);
const BLACK = rgb(0, 0, 0);
const WHITE = rgb(1, 1, 1);
const DARK = rgb(0.1, 0.1, 0.1);
const GREEN = rgb(0.0, 0.5, 0.2);
const RED = rgb(0.7, 0.1, 0.1);
const HEADER_BG = rgb(0.15, 0.15, 0.35);

function fmt(n) {
  if (n == null) return '$0.00';
  const abs = Math.abs(n).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  return n < 0 ? `($${abs})` : `$${abs}`;
}

function fmtDate(ts) {
  if (!ts) return 'N/A';
  return new Date(ts).toLocaleDateString('en-US', { month: '2-digit', day: '2-digit', year: 'numeric' });
}

async function createPage(pdfDoc, title, subtitle) {
  const page = pdfDoc.addPage([792, 612]); // landscape letter
  const bold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  const regular = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const { width, height } = page.getSize();

  // Header bar
  page.drawRectangle({ x: 0, y: height - 50, width, height: 50, color: HEADER_BG });
  page.drawText(title, { x: 20, y: height - 32, size: 16, font: bold, color: WHITE });
  page.drawText(subtitle, { x: 20, y: height - 46, size: 9, font: regular, color: rgb(0.8, 0.8, 0.8) });
  page.drawText('Generated by TaxBot · taxbot.ai', { x: width - 200, y: height - 32, size: 9, font: regular, color: rgb(0.7, 0.7, 0.9) });

  return { page, bold, regular, width, height };
}

export async function generateForm8949(disposals, outputDir, taxYear) {
  const pdfDoc = await PDFDocument.create();
  const capitalGains = disposals.filter(d => d.taxType === 'CAPITAL_GAIN' || (d.gainLoss != null && d.incomeType !== 'ORDINARY'));

  const cols = [
    { label: '(a) Description', width: 140 },
    { label: '(b) Acquired', width: 75 },
    { label: '(c) Sold', width: 75 },
    { label: '(d) Proceeds', width: 80 },
    { label: '(e) Cost Basis', width: 80 },
    { label: '(f) Code', width: 40 },
    { label: '(g) Adjustment', width: 80 },
    { label: '(h) Gain/(Loss)', width: 85 },
  ];

  const rowsPerPage = 22;
  const chunks = [];
  for (let i = 0; i < capitalGains.length; i += rowsPerPage) {
    chunks.push(capitalGains.slice(i, i + rowsPerPage));
  }
  if (chunks.length === 0) chunks.push([]);

  for (const chunk of chunks) {
    const { page, bold, regular, width, height } = await createPage(
      pdfDoc,
      `Form 8949 — Sales and Other Dispositions of Capital Assets (${taxYear})`,
      'Part I: Short-Term  |  Part II: Long-Term'
    );

    let y = height - 70;
    const startX = 20;
    const rowH = 18;

    // Column headers
    let x = startX;
    page.drawRectangle({ x: startX, y: y - 2, width: width - 40, height: rowH, color: rgb(0.9, 0.9, 0.95) });
    for (const col of cols) {
      page.drawText(col.label, { x: x + 2, y: y + 4, size: 7, font: bold, color: DARK });
      x += col.width;
    }
    y -= rowH;

    // Rows
    for (let i = 0; i < chunk.length; i++) {
      const d = chunk[i];
      if (i % 2 === 0) {
        page.drawRectangle({ x: startX, y: y - 2, width: width - 40, height: rowH, color: rgb(0.97, 0.97, 0.97) });
      }

      const values = [
        `${d.quantity?.toFixed(6) || '?'} ${d.asset || '?'}`,
        fmtDate(d.acquiredDate),
        fmtDate(d.timestamp),
        fmt(d.proceeds),
        fmt(d.costBasis),
        d.missingBasis ? 'B' : '',
        d.missingBasis ? fmt(d.costBasis) : '$0.00',
        fmt(d.gainLoss),
      ];

      x = startX;
      for (let j = 0; j < cols.length; j++) {
        const color = j === 7 ? (d.gainLoss >= 0 ? GREEN : RED) : DARK;
        page.drawText(String(values[j] ?? ''), { x: x + 2, y: y + 4, size: 7.5, font: regular, color });
        x += cols[j].width;
      }
      y -= rowH;
    }

    // Totals row
    const totalProceeds = chunk.reduce((s, d) => s + (d.proceeds || 0), 0);
    const totalBasis = chunk.reduce((s, d) => s + (d.costBasis || 0), 0);
    const totalGain = chunk.reduce((s, d) => s + (d.gainLoss || 0), 0);

    y -= 4;
    page.drawRectangle({ x: startX, y: y - 2, width: width - 40, height: rowH, color: HEADER_BG });
    page.drawText('TOTALS', { x: startX + 2, y: y + 4, size: 8, font: bold, color: WHITE });
    const totX = startX + cols[0].width + cols[1].width + cols[2].width;
    page.drawText(fmt(totalProceeds), { x: totX + 2, y: y + 4, size: 8, font: bold, color: WHITE });
    page.drawText(fmt(totalBasis), { x: totX + cols[3].width + 2, y: y + 4, size: 8, font: bold, color: WHITE });
    page.drawText(fmt(totalGain), { x: totX + cols[3].width + cols[4].width + cols[5].width + cols[6].width + 2, y: y + 4, size: 8, font: bold, color: totalGain >= 0 ? rgb(0.8, 1, 0.8) : rgb(1, 0.8, 0.8) });
  }

  const outPath = path.join(outputDir, `form_8949_${taxYear}.pdf`);
  fs.writeFileSync(outPath, await pdfDoc.save());
  return outPath;
}

export async function generateScheduleD(disposals, outputDir, taxYear) {
  const pdfDoc = await PDFDocument.create();
  const { page, bold, regular, width, height } = await createPage(
    pdfDoc,
    `Schedule D — Capital Gains and Losses (${taxYear})`,
    'Summary of Form 8949 · Attach to Form 1040'
  );

  const stGains = disposals.filter(d => d.taxType === 'CAPITAL_GAIN' && !d.isLongTerm);
  const ltGains = disposals.filter(d => d.taxType === 'CAPITAL_GAIN' && d.isLongTerm);

  const stTotal = stGains.reduce((s, d) => s + (d.gainLoss || 0), 0);
  const ltTotal = ltGains.reduce((s, d) => s + (d.gainLoss || 0), 0);
  const netTotal = stTotal + ltTotal;

  let y = height - 80;
  const drawSection = (label, items, total, isLT) => {
    page.drawText(label, { x: 30, y, size: 12, font: bold, color: HEADER_BG });
    y -= 20;

    const headers = ['Description', 'Proceeds', 'Cost Basis', 'Gain/(Loss)', 'Term'];
    const widths = [250, 120, 120, 120, 80];
    let x = 30;
    page.drawRectangle({ x: 30, y: y - 2, width: width - 60, height: 18, color: rgb(0.9, 0.9, 0.95) });
    for (let i = 0; i < headers.length; i++) {
      page.drawText(headers[i], { x: x + 3, y: y + 4, size: 9, font: bold, color: DARK });
      x += widths[i];
    }
    y -= 18;

    for (const d of items.slice(0, 15)) {
      x = 30;
      const row = [
        `${d.quantity?.toFixed(4)} ${d.asset}`,
        fmt(d.proceeds),
        fmt(d.costBasis),
        fmt(d.gainLoss),
        isLT ? 'Long-term' : 'Short-term',
      ];
      for (let i = 0; i < row.length; i++) {
        const color = i === 3 ? (d.gainLoss >= 0 ? GREEN : RED) : DARK;
        page.drawText(row[i], { x: x + 3, y: y + 4, size: 8.5, font: regular, color });
        x += widths[i];
      }
      y -= 16;
    }
    if (items.length > 15) {
      page.drawText(`... and ${items.length - 15} more transactions (see Form 8949)`, { x: 33, y: y + 4, size: 8, font: regular, color: GRAY });
      y -= 16;
    }

    // Subtotal
    page.drawRectangle({ x: 30, y: y - 2, width: width - 60, height: 18, color: rgb(0.85, 0.85, 0.92) });
    page.drawText(`${isLT ? 'Long-term' : 'Short-term'} Net: ${fmt(total)}`, { x: 33, y: y + 4, size: 10, font: bold, color: total >= 0 ? GREEN : RED });
    y -= 30;
  };

  drawSection('Part I — Short-Term Capital Gains and Losses', stGains, stTotal, false);
  drawSection('Part II — Long-Term Capital Gains and Losses', ltGains, ltTotal, true);

  // Net summary box
  y -= 10;
  page.drawRectangle({ x: 30, y: y - 40, width: width - 60, height: 50, color: HEADER_BG });
  page.drawText('Net Capital Gain/(Loss) Summary', { x: 40, y: y + 4, size: 12, font: bold, color: WHITE });
  page.drawText(`Short-term: ${fmt(stTotal)}   Long-term: ${fmt(ltTotal)}   NET TOTAL: ${fmt(netTotal)}`, {
    x: 40, y: y - 20, size: 11, font: bold, color: netTotal >= 0 ? rgb(0.6, 1, 0.6) : rgb(1, 0.6, 0.6),
  });

  const outPath = path.join(outputDir, `schedule_d_${taxYear}.pdf`);
  fs.writeFileSync(outPath, await pdfDoc.save());
  return outPath;
}

export async function generateSchedule1(disposals, outputDir, taxYear) {
  const pdfDoc = await PDFDocument.create();
  const { page, bold, regular, width, height } = await createPage(
    pdfDoc,
    `Schedule 1 — Additional Income (${taxYear})`,
    'Part I: Ordinary Income from Crypto · Staking, Rewards, Airdrops'
  );

  const income = disposals.filter(d => d.taxType === 'ORDINARY_INCOME' || d.incomeType === 'ORDINARY');
  const totalIncome = income.reduce((s, d) => s + (d.gainLoss || d.proceeds || 0), 0);

  let y = height - 80;
  const cols = ['Date', 'Asset', 'Quantity', 'FMV at Receipt', 'Income Type', 'Amount'];
  const widths = [90, 80, 100, 120, 150, 120];

  page.drawRectangle({ x: 30, y: y - 2, width: width - 60, height: 18, color: rgb(0.9, 0.9, 0.95) });
  let x = 30;
  for (let i = 0; i < cols.length; i++) {
    page.drawText(cols[i], { x: x + 3, y: y + 4, size: 9, font: bold, color: DARK });
    x += widths[i];
  }
  y -= 18;

  for (const d of income) {
    x = 30;
    const row = [
      fmtDate(d.timestamp),
      d.asset,
      d.quantity?.toFixed(6) || '?',
      fmt(d.price * d.quantity),
      d.note || 'Staking reward',
      fmt(d.gainLoss || d.proceeds),
    ];
    for (let i = 0; i < row.length; i++) {
      page.drawText(String(row[i]), { x: x + 3, y: y + 4, size: 8.5, font: regular, color: DARK });
      x += widths[i];
    }
    y -= 16;
    if (y < 60) break;
  }

  y -= 10;
  page.drawRectangle({ x: 30, y: y - 30, width: width - 60, height: 40, color: HEADER_BG });
  page.drawText(`Total Ordinary Income from Crypto: ${fmt(totalIncome)}`, {
    x: 40, y: y - 12, size: 12, font: bold, color: WHITE,
  });
  page.drawText('Report on Schedule 1, Line 8z — "Other Income"', {
    x: 40, y: y - 26, size: 9, font: regular, color: rgb(0.8, 0.8, 0.8),
  });

  const outPath = path.join(outputDir, `schedule_1_${taxYear}.pdf`);
  fs.writeFileSync(outPath, await pdfDoc.save());
  return outPath;
}
